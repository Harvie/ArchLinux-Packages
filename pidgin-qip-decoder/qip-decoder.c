static int HexValue(char c) {
	if (c>='0' && c<='9') {
	    return c-'0';
	} else if (c>='A' && c<='F') {
	    return c-('A'-0x0A);
	} else if (c>='a' && c<='f') {
	    return c-('a'-0x0A);
	} else {
	    return -1;
	}
}

static int UTF8Char(char* str, unsigned short code) {
	if (code<=0x7F) {
	    if (str) {
			str[0] = code;
		}
		return 1;
	} else if (code<=0x7FF) {
		if (str) {
			str[0] = 0xC0 | (code >> 6);
			str[1] = 0x80 | (code & 0x3F);
		}
	    return 2;
	} else {
	    if (str) {
			str[0] = 0xE0 | (code >> 12);
			str[1] = 0x80 | ((code >> 6) & 0x3F);
			str[2] = 0x80 | (code & 0x3F);
		}
		return 3;
	}
}

